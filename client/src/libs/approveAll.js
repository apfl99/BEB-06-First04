// 현재 계정 주소 가져오기
async function getCurrentAccount() {
  const accounts = await window.ethereum.request({
    method: "eth_requestAccounts",
  });
  return accounts[0];
}

//작성한 거래 컨트랙에 토큰 핸들링 권한 부여
export async function approveAll(_collection) {
  // 거래 컨트랙
  const contractAddr = "0x95602fd56336Ca99b2a08143B47838B2dC0F27e3"
  await loadWeb3();
  window.contract = await loadContractNFT(_collection); // 컨트랙 정보 가져오기
  const account = await getCurrentAccount(); // 계정 정보 가져오기

  //토큰 권한 승인
  let result  = await window.contract.methods
    .setApprovalForAll(contractAddr, true)
    .send({ from: account }); // mintNFT 트랜잭션 보내기


  return result;

}

