// 현재 계정 주소 가져오기
async function getCurrentAccount() {
  const accounts = await window.ethereum.request({
    method: "eth_requestAccounts",
  });
  return accounts[0];
}

export async function mint(tokenURI) {
  console.log(tokenURI);
  await loadWeb3();
  window.contract = await loadContract(); // 컨트랙 정보 가져오기
  const account = await getCurrentAccount(); // 계정 정보 가져오기
  console.log(account);
  let result = await window.contract.methods
    .mintNFT(account, tokenURI)
    .send({ from: account }); // mintNFT 트랜잭션 보내기
  return result;
}
